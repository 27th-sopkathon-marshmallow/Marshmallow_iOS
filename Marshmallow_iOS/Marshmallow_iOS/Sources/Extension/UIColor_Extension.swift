import Foundation
import UIKit

extension UIColor {
    @nonobjc class var mypink: UIColor {
        return UIColor(red: 234.0 / 255.0, green: 68.0 / 255.0, blue: 90.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var palegrey: UIColor {
        return UIColor(red: 242.0 / 255.0, green: 242.0 / 255.0, blue: 247.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightgrey: UIColor {
        return UIColor(white: 168.0 / 255.0, alpha: 1.0)
      }

    
    @nonobjc class var lightpink: UIColor {
       return UIColor(red: 1.0, green: 235.0 / 255.0, blue: 235.0 / 255.0, alpha: 1.0)
     }

    @nonobjc class var darkgrey: UIColor {
        return UIColor(white: 125.0 / 255.0, alpha: 1.0)
      }
    
}


